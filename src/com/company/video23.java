package com.company;
// The for Loop in Java

public class video23 {
    public static void main(String[] args) {
        System.out.println("For Loop :");
        for (int a = 1; a<=10; a++) {
            System.out.println(a);
        }
        // 2n+1 = Odd Numbers
        // 2n = Even Numbers
        System.out.println("Odd Numbers using For Loop : ");
        for (int b=0; b<5;b++){
            int Num = 1;
            System.out.println(2*b+Num);

        }
        System.out.println("Decrement using For Loop : ");
        for (int c=5; c>0; c--){
            System.out.println(c);
        }

    }
}
